	assert.IsType(t, &models.PokeathlonStat{}, rPokeathlon, "Expected to have type 'PokeathlonStat' struct.")
	assert.IsType(t, &models.Pokemon{}, rPokemon, "Expected to have type 'Pokemon' struct.")
