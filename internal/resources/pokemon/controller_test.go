	assert.IsType(t, &models.Stat{}, rStats, "Expected to have type 'Stat' struct.")
	assert.IsType(t, &models.Type{}, rTypes, "Expected to have type 'Type' struct.")
	assert.Error(t, err, "Expected an error to be thrown.")
