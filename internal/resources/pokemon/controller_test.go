	assert.IsType(t, &models.Ability{}, rAbilities, "Expected to have type 'Ability' struct.")
	assert.IsType(t, &models.Characteristic{}, rCharacteristics, "Expected to have type 'Characteristic' struct.")
	assert.IsType(t, &models.EggGroup{}, rEgg, "Expected to have type 'EggGroup' struct.")
	assert.IsType(t, &models.Gender{}, rGender, "Expected to have type 'Gender' struct.")
	assert.IsType(t, &models.GrowthRate{}, rGrowth, "Expected to have type 'GrowthRate' struct.")
